#!/bin/bash

# common shell functions for all layers of docker scripts

docker_info() {
  # param 1 is $1 - docker info

  echo "
  !!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
  !!                                                                                                                                           !!
  !!  !!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!  !!
  !!  !!                                                                                                                                   !!  !!
  !!  !!                                                                                                                                   !!  !!";

  printf "  !!  !! %-129s !!  !!" "$1";

  echo "
  !!  !!                                                                                                                                   !!  !!
  !!  !!                                                                                                                                   !!  !!
  !!  !!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!  !!
  !!                                                                                                                                           !!
  !!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
  ";
}

part_info() {
  # param 1 is $1 - part number
  # param 2 is $2 - part functionality
  echo "
  ***********************************************************************************************************************************************
  ***********************************************************************************************************************************************
  ***********************************************************************************************************************************************
  ***********************************************************************************************************************************************
  ***********************************************************************************************************************************************"
  
  if [[ $# -eq 2 ]] && [[ !(-z "$2") ]]; then 
    echo "
    $1

        $2
    ";
  else 
    echo "
    $1
    ";
  fi

  echo "
  ***********************************************************************************************************************************************
  ***********************************************************************************************************************************************
  ***********************************************************************************************************************************************
  ***********************************************************************************************************************************************
  ***********************************************************************************************************************************************
  "
}

